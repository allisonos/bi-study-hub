export const getDailyMotivational = (genre = 'any') => {
  const today = new Date().toISOString().split('T')[0];
  const seed = today.split('-').reduce((a, b) => a + parseInt(b), 0);

  // Filter by genre or use any
  const filtered = genre === 'any'
    ? motivationalItems
    : motivationalItems.filter(i => i.genre === 'any' || i.genre === genre);

  const pool = filtered.length > 0 ? filtered : motivationalItems;
  return pool[seed % pool.length];
};
